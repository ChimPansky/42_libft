#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t	slen;
	char	*result;

	slen = ft_strlen(s);
	result = (char *)ft_calloc(sizeof(char), slen + 1);
	if (!result)
		return (NULL);
	ft_strlcpy(result, s, slen);
	return (result);
}
