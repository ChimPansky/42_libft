#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*result;
	size_t	total_len;

	if (!s1 || !s2)
		return (NULL);
	total_len = ft_strlen(s1) + ft_strlen(s2) + 1;
	result = (char *)ft_calloc(sizeof(char), total_len);
	if (!result)
		return (NULL);
	ft_strlcat(result, s1, total_len);
	ft_strlcat(result, s2, total_len);
	return (result);
}
