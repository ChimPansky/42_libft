#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char				*str;
	const unsigned char	myc = (const unsigned char) c;

	str = (char *)s;
	while (*str)
	{
		if (*str == myc)
			return (str);
		str++;
	}
	if (!myc)
		return (str);
	return (NULL);
}
