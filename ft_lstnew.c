#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*new_node;

	new_node = (t_list *)ft_calloc(sizeof(t_list*), 1);
	if (!new_node)
		return (NULL);
	new_node->content = content;
	new_node->next = NULL;

	return (new_node);
}
