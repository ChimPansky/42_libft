#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*new_list;

	new_list = (t_list *)ft_calloc(sizeof(t_list*), 1);
	if (!new_list)
		return (NULL);
	new_list->content = content;
	new_list->next = NULL;

	return (new_list);
}
