#include "libft.h"

ssize_t	ft_putendl_fd(char *s, int fd)
{
	ssize_t	bytes_written;

	bytes_written = 0;
	if (s)
		bytes_written += ft_putstr_fd(s, fd);
	if (bytes_written > -1)
		bytes_written += ft_putchar_fd('\n', fd);
	return (bytes_written);
}
